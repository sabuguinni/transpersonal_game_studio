UAudio_VoicelineIntegration::UAudio_VoicelineIntegration()
{
    PrimaryComponentTick.bCanEverTick = false;
    
    MasterVoicelineVolume = 1.0f;
    bVoicelinesEnabled = true;
    MinTimeBetweenVoicelines = 5.0f;
    LastVoicelineTime = 0.0f;
    
    VoicelineAudioComponent = nullptr;
}

void UAudio_VoicelineIntegration::BeginPlay()
{
    Super::BeginPlay();
    
    InitializeVoicelineSystem();
    LoadPresetVoicelines();
}

void UAudio_VoicelineIntegration::InitializeVoicelineSystem()
{
    // Create audio component for voiceline playback
    if (!VoicelineAudioComponent)
    {
        VoicelineAudioComponent = NewObject<UAudioComponent>(this);
        if (VoicelineAudioComponent)
        {
            VoicelineAudioComponent->SetVolumeMultiplier(MasterVoicelineVolume);
            VoicelineAudioComponent->bAutoActivate = false;
            VoicelineAudioComponent->AttachToComponent(GetOwner()->GetRootComponent(), 
                FAttachmentTransformRules::KeepRelativeTransform);
        }
    }
    
    // Initialize cooldown tracking
    TriggerCooldowns.Empty();
    for (int32 i = 0; i < (int32)EAudio_VoicelineTrigger::OnSurvival + 1; ++i)
    {
        EAudio_VoicelineTrigger TriggerType = (EAudio_VoicelineTrigger)i;
        TriggerCooldowns.Add(TriggerType, 0.0f);
    }
    
    UE_LOG(LogTemp, Log, TEXT("Audio_VoicelineIntegration: System initialized"));
}

