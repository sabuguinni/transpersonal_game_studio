void UAudio_VoicelineIntegration::TriggerVoiceline(EAudio_VoicelineTrigger TriggerType, const FVector& Location)
{
    if (!bVoicelinesEnabled || !CanTriggerVoiceline(TriggerType))
    {
        return;
    }
    
    FAudio_VoicelineData SelectedVoiceline = GetRandomVoicelineByTrigger(TriggerType);
    if (!SelectedVoiceline.VoicelineText.IsEmpty())
    {
        PlayVoicelineInternal(SelectedVoiceline, Location);
        
        // Update cooldown
        TriggerCooldowns[TriggerType] = GetWorld()->GetTimeSeconds() + SelectedVoiceline.CooldownTime;
        LastVoicelineTime = GetWorld()->GetTimeSeconds();
        
        UE_LOG(LogTemp, Log, TEXT("Audio_VoicelineIntegration: Triggered %s voiceline"), 
            *UEnum::GetValueAsString(TriggerType));
    }
}

void UAudio_VoicelineIntegration::PlayVoicelineByType(EAudio_VoicelineType VoicelineType, const FVector& Location)
{
    if (!bVoicelinesEnabled)
    {
        return;
    }
    
    for (const FAudio_VoicelineData& Voiceline : VoicelineDatabase)
    {
        if (Voiceline.VoicelineType == VoicelineType)
        {
            PlayVoicelineInternal(Voiceline, Location);
            LastVoicelineTime = GetWorld()->GetTimeSeconds();
            break;
        }
    }
}

void UAudio_VoicelineIntegration::PlayVoicelineInternal(const FAudio_VoicelineData& VoicelineData, const FVector& Location)
{
    if (!VoicelineAudioComponent)
    {
        UE_LOG(LogTemp, Warning, TEXT("Audio_VoicelineIntegration: No audio component available"));
        return;
    }
    
    // Stop any currently playing voiceline
    StopCurrentVoiceline();
    
    // For now, we'll use a placeholder sound since we can't directly load from URL
    // In a full implementation, this would load the audio from the URL
    UE_LOG(LogTemp, Log, TEXT("Audio_VoicelineIntegration: Playing voiceline: %s"), 
        *VoicelineData.VoicelineText);
    UE_LOG(LogTemp, Log, TEXT("Audio_VoicelineIntegration: Audio URL: %s"), 
        *VoicelineData.AudioURL);
    
    // Set timer to call OnVoicelineFinished after the duration
    GetWorld()->GetTimerManager().SetTimer(VoicelineTimerHandle, 
        this, &UAudio_VoicelineIntegration::OnVoicelineFinished, 
        VoicelineData.Duration, false);
}

void UAudio_VoicelineIntegration::StopCurrentVoiceline()
{
    if (VoicelineAudioComponent && VoicelineAudioComponent->IsPlaying())
    {
        VoicelineAudioComponent->Stop();
    }
    
    GetWorld()->GetTimerManager().ClearTimer(VoicelineTimerHandle);
}

bool UAudio_VoicelineIntegration::CanTriggerVoiceline(EAudio_VoicelineTrigger TriggerType) const
{
    float CurrentTime = GetWorld()->GetTimeSeconds();
    
    // Check global cooldown
    if (CurrentTime - LastVoicelineTime < MinTimeBetweenVoicelines)
    {
        return false;
    }
    
    // Check specific trigger cooldown
    if (TriggerCooldowns.Contains(TriggerType))
    {
        return CurrentTime >= TriggerCooldowns[TriggerType];
    }
    
    return true;
}

void UAudio_VoicelineIntegration::SetVoicelineVolume(float Volume)
{
    MasterVoicelineVolume = FMath::Clamp(Volume, 0.0f, 1.0f);
    
    if (VoicelineAudioComponent)
    {
        VoicelineAudioComponent->SetVolumeMultiplier(MasterVoicelineVolume);
    }
}

void UAudio_VoicelineIntegration::EnableVoicelines(bool bEnabled)
{
    bVoicelinesEnabled = bEnabled;
    
    if (!bEnabled)
    {
        StopCurrentVoiceline();
    }
}

FAudio_VoicelineData UAudio_VoicelineIntegration::GetRandomVoicelineByTrigger(EAudio_VoicelineTrigger TriggerType) const
{
    TArray<FAudio_VoicelineData> MatchingVoicelines;
    
    for (const FAudio_VoicelineData& Voiceline : VoicelineDatabase)
    {
        if (Voiceline.TriggerType == TriggerType)
        {
            MatchingVoicelines.Add(Voiceline);
        }
    }
    
    if (MatchingVoicelines.Num() > 0)
    {
        int32 RandomIndex = FMath::RandRange(0, MatchingVoicelines.Num() - 1);
        return MatchingVoicelines[RandomIndex];
    }
    
    return FAudio_VoicelineData();
}

void UAudio_VoicelineIntegration::AddVoicelineToDatabase(const FAudio_VoicelineData& NewVoiceline)
{
    VoicelineDatabase.Add(NewVoiceline);
    UE_LOG(LogTemp, Log, TEXT("Audio_VoicelineIntegration: Added new voiceline to database"));
}

