APrehistoricAudioSystem::APrehistoricAudioSystem()
{
    PrimaryActorTick.bCanEverTick = true;
    PrimaryActorTick.TickInterval = 0.1f; // 10Hz tick — audio doesn't need 60Hz

    // Ambient audio component — jungle/environment layers
    AmbientAudioComponent = CreateDefaultSubobject<UAudioComponent>(TEXT("AmbientAudio"));
    AmbientAudioComponent->SetupAttachment(RootComponent);
    AmbientAudioComponent->bAutoActivate = false;
    AmbientAudioComponent->VolumeMultiplier = 1.0f;

    // Danger audio component — music stingers, tension layers
    DangerAudioComponent = CreateDefaultSubobject<UAudioComponent>(TEXT("DangerAudio"));
    DangerAudioComponent->SetupAttachment(RootComponent);
    DangerAudioComponent->bAutoActivate = false;
    DangerAudioComponent->VolumeMultiplier = 0.0f; // starts silent

    // Voice audio component — survivor narration lines
    VoiceAudioComponent = CreateDefaultSubobject<UAudioComponent>(TEXT("VoiceAudio"));
    VoiceAudioComponent->SetupAttachment(RootComponent);
    VoiceAudioComponent->bAutoActivate = false;

    // Pre-populate voice lines from ElevenLabs TTS (generated by Audio Agent #16)
    // These URLs are from Supabase Storage — loaded at runtime
    FAudio_VoiceLine NightWarning;
    NightWarning.LineID = TEXT("night_warning");
    NightWarning.AudioURL = TEXT("https://thdlkizjbpwdndtggleb.supabase.co/storage/v1/object/public/game-assets/tts/1782523071356_Survivor_NightWarning.mp3");
    NightWarning.Subtitle = TEXT("Night is the most dangerous time. Keep the fire burning. Never let it die.");
    NightWarning.Duration = 15.0f;
    SurvivorVoiceLines.Add(NightWarning);

    FAudio_VoiceLine WaterWarning;
    WaterWarning.LineID = TEXT("water_warning");
    WaterWarning.AudioURL = TEXT("https://thdlkizjbpwdndtggleb.supabase.co/storage/v1/object/public/game-assets/tts/1782523079223_Survivor_WaterWarning.mp3");
    WaterWarning.Subtitle = TEXT("The watering holes are shared. If the birds stop singing, something large is already there.");
    WaterWarning.Duration = 18.0f;
    SurvivorVoiceLines.Add(WaterWarning);
}

void APrehistoricAudioSystem::BeginPlay()
{
    Super::BeginPlay();

    // Start ambient audio system
    if (AmbientAudioComponent)
    {
        AmbientAudioComponent->Activate();
    }

    UE_LOG(LogTemp, Log, TEXT("PrehistoricAudioSystem: Initialized. Danger=%d, Biome=%d, Time=%d"),
        (int32)CurrentDangerLevel, (int32)CurrentBiome, (int32)CurrentTimeOfDay);
}

void APrehistoricAudioSystem::Tick(float DeltaTime)
{
    Super::Tick(DeltaTime);

    // Update silence window timer
    if (bInSilenceWindow)
    {
        SilenceWindowTimer -= DeltaTime;
        if (SilenceWindowTimer <= 0.0f)
        {
            bInSilenceWindow = false;
            SilenceWindowTimer = 0.0f;
        }
    }

    // Update danger stinger cooldown
    if (DangerStingerCooldown > 0.0f)
    {
        DangerStingerCooldown -= DeltaTime;
    }

    // Throttled ambient update
    AmbientUpdateTimer += DeltaTime;
    if (AmbientUpdateTimer >= AmbientUpdateInterval)
    {
        AmbientUpdateTimer = 0.0f;
        UpdateDangerFromProximity(AmbientUpdateInterval);
        UpdateAmbientLayers(AmbientUpdateInterval);
    }

    // Blend music layers every tick for smooth transitions
    BlendMusicLayers(DeltaTime);
}

void APrehistoricAudioSystem::UpdateDangerFromProximity(float DeltaTime)
{
    UWorld* World = GetWorld();
    if (!World) return;

    APawn* PlayerPawn = UGameplayStatics::GetPlayerPawn(World, 0);
    if (!PlayerPawn) return;

    FVector PlayerLocation = PlayerPawn->GetActorLocation();
    float ClosestDino = 99999.0f;

    // Scan all actors for dinosaur pawns
    TArray<AActor*> AllActors;
    UGameplayStatics::GetAllActorsOfClass(World, APawn::StaticClass(), AllActors);

    for (AActor* Actor : AllActors)
    {
        if (Actor == PlayerPawn) continue;

        FString ActorName = Actor->GetName().ToLower();
        // Check if this is a dinosaur (by name convention from other agents)
        bool bIsDino = ActorName.Contains(TEXT("dino")) ||
                       ActorName.Contains(TEXT("trex")) ||
                       ActorName.Contains(TEXT("raptor")) ||
                       ActorName.Contains(TEXT("brach")) ||
                       ActorName.Contains(TEXT("tricera")) ||
                       ActorName.Contains(TEXT("edmonto"));

        if (bIsDino)
        {
            float Dist = FVector::Dist(PlayerLocation, Actor->GetActorLocation());
            ClosestDino = FMath::Min(ClosestDino, Dist);
        }
    }

    NearestDinosaurDistance = ClosestDino;
    DangerIntensity = CalculateDangerIntensity();

    // Auto-set danger level based on proximity
    EAudio_DangerLevel NewLevel = EAudio_DangerLevel::Safe;
    if (ClosestDino < 500.0f)
        NewLevel = EAudio_DangerLevel::Combat;
    else if (ClosestDino < 1200.0f)
        NewLevel = EAudio_DangerLevel::Threatened;
    else if (ClosestDino < 2500.0f)
        NewLevel = EAudio_DangerLevel::Cautious;

    if (NewLevel != CurrentDangerLevel)
    {
        SetDangerLevel(NewLevel);
    }
}

