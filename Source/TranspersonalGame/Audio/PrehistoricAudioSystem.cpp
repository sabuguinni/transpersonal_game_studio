void APrehistoricAudioSystem::BlendMusicLayers(float DeltaTime)
{
    if (!DangerAudioComponent) return;

    // Target volume based on danger intensity
    float TargetVolume = DangerIntensity;

    // During silence window (pre-attack), cut music to zero
    if (bInSilenceWindow)
    {
        TargetVolume = 0.0f;
    }

    // Smooth blend
    float CurrentVolume = DangerAudioComponent->VolumeMultiplier;
    float NewVolume = FMath::FInterpTo(CurrentVolume, TargetVolume, DeltaTime, MusicTransitionSpeed);
    DangerAudioComponent->SetVolumeMultiplier(NewVolume);
}

float APrehistoricAudioSystem::CalculateDangerIntensity() const
{
    // Map danger level to 0-1 intensity
    switch (CurrentDangerLevel)
    {
        case EAudio_DangerLevel::Safe:       return 0.0f;
        case EAudio_DangerLevel::Cautious:   return 0.25f;
        case EAudio_DangerLevel::Threatened: return 0.6f;
        case EAudio_DangerLevel::Combat:     return 1.0f;
        case EAudio_DangerLevel::Fleeing:    return 0.85f;
        default:                             return 0.0f;
    }
}

void APrehistoricAudioSystem::SetDangerLevel(EAudio_DangerLevel NewLevel)
{
    if (NewLevel == CurrentDangerLevel) return;

    EAudio_DangerLevel OldLevel = CurrentDangerLevel;
    CurrentDangerLevel = NewLevel;
    DangerIntensity = CalculateDangerIntensity();

    UE_LOG(LogTemp, Log, TEXT("PrehistoricAudioSystem: Danger level changed %d -> %d (intensity=%.2f)"),
        (int32)OldLevel, (int32)NewLevel, DangerIntensity);

    // Trigger silence window before escalating to combat (Walter Murch principle)
    if (NewLevel == EAudio_DangerLevel::Combat && OldLevel == EAudio_DangerLevel::Threatened)
    {
        TriggerSilenceWindow(SilenceBeforeAttackSeconds);
    }

    // Trigger stinger on escalation
    if ((int32)NewLevel > (int32)OldLevel)
    {
        TriggerDangerStinger(NewLevel);
    }
}

void APrehistoricAudioSystem::SetTimeOfDay(EAudio_TimeOfDay NewTime)
{
    if (NewTime == CurrentTimeOfDay) return;
    CurrentTimeOfDay = NewTime;

    UE_LOG(LogTemp, Log, TEXT("PrehistoricAudioSystem: Time of day changed to %d"), (int32)NewTime);
}

void APrehistoricAudioSystem::SetBiome(EAudio_BiomeType NewBiome)
{
    if (NewBiome == CurrentBiome) return;
    CurrentBiome = NewBiome;

    UE_LOG(LogTemp, Log, TEXT("PrehistoricAudioSystem: Biome changed to %d"), (int32)NewBiome);
}

void APrehistoricAudioSystem::TriggerDangerStinger(EAudio_DangerLevel Level)
{
    if (DangerStingerCooldown > 0.0f) return;

    // Find matching danger cue
    for (const FAudio_DangerCue& Cue : DangerCues)
    {
        if (Cue.TriggerLevel == Level && Cue.StingerSound)
        {
            if (DangerAudioComponent)
            {
                DangerAudioComponent->SetSound(Cue.StingerSound);
                DangerAudioComponent->Play();
                DangerStingerCooldown = Cue.CooldownSeconds;
                UE_LOG(LogTemp, Log, TEXT("PrehistoricAudioSystem: Playing danger stinger for level %d"), (int32)Level);
            }
            break;
        }
    }
}

void APrehistoricAudioSystem::PlayVoiceLine(const FString& LineID)
{
    for (FAudio_VoiceLine& Line : SurvivorVoiceLines)
    {
        if (Line.LineID == LineID && !Line.bPlayed)
        {
            // In production: load audio from Line.AudioURL and play via VoiceAudioComponent
            // For now, log the subtitle as feedback
            UE_LOG(LogTemp, Log, TEXT("PrehistoricAudioSystem: Voice line [%s]: %s"),
                *LineID, *Line.Subtitle);
            Line.bPlayed = true;
            break;
        }
    }
}

void APrehistoricAudioSystem::TriggerSilenceWindow(float Duration)
{
    bInSilenceWindow = true;
    SilenceWindowTimer = Duration;

    UE_LOG(LogTemp, Log, TEXT("PrehistoricAudioSystem: SILENCE WINDOW triggered for %.1fs (pre-attack tension)"), Duration);
}

