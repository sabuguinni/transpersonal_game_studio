UArch_DwellingSystem::UArch_DwellingSystem()
{
    PrimaryComponentTick.bCanEverTick = false;
    
    // Initialize dwelling configurations
    InitializeDwellingTypes();
}

void UArch_DwellingSystem::BeginPlay()
{
    Super::BeginPlay();
    
    UE_LOG(LogTemp, Warning, TEXT("Arch_DwellingSystem: System initialized"));
    
    // Auto-generate dwellings if enabled
    if (bAutoGenerateDwellings)
    {
        GenerateDwellingsInBiomes();
    }
}

